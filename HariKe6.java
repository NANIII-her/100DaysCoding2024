public class HariKe6 {
    public static void main(String[] args){
        System.out.println("Ini adalah proyek hari ke-6 saya");

        //Membuat sebuah variabel deklarasi
        byte x, y, z, hasil;
        x = 7;
        y = 4;
        z = 9;

        hasil = (byte) (x * (z - y));

        System.out.printf("%d (%d - %d) = %d", x, y, z, hasil);
    }
}
