public class HariKe15 {
    public static void main(String[] args){

        System.out.println("Ini adalah proyek hari ke-15");

        //Tipe data double
        int a, b;
        a = 7;
        b = 4;

        //Mengunakan tipe data double sebagai hasil pembagian, nanti hasilnya akan berbentuk bilangan koma 
        double hasil = (double) a / b;  

        System.out.println("Hasilnya = " + hasil);
    }
}
