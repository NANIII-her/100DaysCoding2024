import java.util.Scanner;

public class HariKe37{
    public static void main(String[] args) {
        
        Scanner sc = new Scanner(System.in);

        System.out.print("A = ");
        int a = sc.nextInt();

        System.out.print("B = ");
        int b = sc.nextInt();

        System.out.println("Hasil jumlahnya adalah " + (a + b));
        System.out.println("Hasil kurangnya adalah " + (a - b));
        
    }
}
