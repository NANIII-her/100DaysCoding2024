public class HarKe14 {
    public static void main(String[] args) {
        
        System.out.println("Maaf saya terlambat");
        System.out.println("Ini adalah proyek hari ke-14");

        //Tipe data long
        long wawa, heri, flora, juhariana, fauziah, hasil;

        wawa = 1000;
        heri = 10000; 
        flora = 100000;
        juhariana = 1000000;
        fauziah = 10000000;

        hasil = (juhariana * wawa) / fauziah + flora - heri;

        System.out.println("Hasilnya adalah " + hasil); //Hasilnya 90.100
    }
}
